"""
Tipos int e float
int -> numero inteiro
o tipo int representa qualquer numero positivo ou negativo
int sem sinal é considerado positivo
"""
print(11) # int 
print(-11) # int
print(0)

"""
float -> Numero com ponto flutuante
O tipo float representa qualquer número positivo ou negativo
com ponto flutuante.
float sem sinal é considerado positivo.
"""
print(1.1, 10.11)
print(0.0, 1.5)

"""
A funcao type mostra o tupo que o python inferiu o valor
"""
print(type("Victor"))
print(type(1))
print(type(1.1), type(-1.1), type(0.0))
