"""
Python é uma linguagem de programação de tipagem dinâmica e forte
DocString
Strings sao textos que estão dentro de aspas
str -> string -> texto
"""

# Aspas simples
print('Victor Roza')
print('Victor "Roza"')

# Aspas duplas
print("Victor Roza")
print("Victor 'Roza'")

# Escape
print("Victor \"Roza\"")

# r - expressoes regulares
print(r"Victor \"Roza\"")
