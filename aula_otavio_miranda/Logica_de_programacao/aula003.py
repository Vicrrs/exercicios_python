# Tipo de dado bool (booleano)
# Ao questinonar algo em um programa
# só existem duas respostas possiveis
# Sim (true) ou Não (False)
# Existem vários operadores para "questionar"
# Dentre eles o ==, que é um operador lógico que
# questiona se um valor é igual ao outro
print(10 == 10)
print(10 == 11)
print(type(10 == 11))